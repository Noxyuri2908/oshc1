    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $obj = Comment::find($id);
        if($obj == null){
            Session::flash('error-comment', 'Không tìm thấy dữ liệu.');  
            return redirect()->route('comment.index');  
        }
        $obj->delete();
        Session::flash('success-comment', 'Xóa comment thành công.');  
        return redirect()->route('comment.index');  
    }

    public function mutileUpdate(Request $request)
    {
        $status = $request->status;
        $data = $request->data_selected;
        $data = explode(",", $data[0]);
        if($status != 2)
        {
            for($i = 0; $i < sizeof($data); $i++)
            {
                $obj = Comment::find($data[$i]);
                if($obj != null)
                {
                    $obj->status = $status;
                    $obj->update();
                }
            }
        }else{
            for($i = 0; $i < sizeof($data); $i++)
            {
                $obj = Comment::find($data[$i]);
                if($obj != null)
                {
                    $obj->delete();
                }
            }
        }       
        Session::flash('success-comment', 'Update đồng loạt thành công.');
        return redirect()->route('comment.index');
    }
